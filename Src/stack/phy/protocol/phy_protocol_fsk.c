static uint16_t PhyFrame_calcFcs_FSK( const uint8_t* base, uint8_t length )
{
    WDG_Refresh();

    uint16_t crc = 0;

    /* calc crc */
    for( uint8_t idx = 0; idx < length; idx++ )
    {
        crc ^= base[idx];

#if 1
        crc = (crc >> 8) ^ crc16Table[crc & 0x00FF];
#else
        for( uint8_t i=0; i<8; i++ )
        {
            if( crc & 0x0001 )
            {
                crc >>= 1;
                crc ^= 0x8408;
            }
            else
            {
                crc >>= 1;
            }
        }
#endif
    }

    return crc;
}

static void PhyFrame_bitSwap_FSK( uint8_t* base, uint8_t length )
{
    WDG_Refresh();

    /* data whitening */
    for( uint8_t idx = 0; idx < length; idx++ )
    {
        base[idx] = swapBitTable[base[idx]];
    }
}

