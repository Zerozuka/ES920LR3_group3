void PhyFrame_makeFrame_FSK( phyFrame_FSK_t* frame, uint8_t sduSize )
{
    phyHeader_FSK_t*    header  = &frame->header;
    phyFooter_FSK_t*    footer  = PhyFrame_getFooter_FSK( frame, sduSize );

    // Calc fcs
    const uint16_t      fcs     = PhyFrame_calcFcs_FSK( frame->sdu.data, sduSize );

    // Fill header
    memset( header, 0, sizeof(*header) );
    header->modeSwitch      = PhyDataModeSwitch_DISABLE;
    header->fcsType         = PhyFcsType_CRC_2OCTET;
    header->dataWhitening   = PhyDataWhitening_ENABLE;
    header->frameLength     = PhyFrameLengthField_FSK(sduSize);
    PhyFrame_bitSwap_FSK( header->byteAccess, 1 );  // LSB first

    // Fill footer
    memset( footer, 0, sizeof(*footer) );
    octet2_set( &footer->fcs, fcs );

    // Whitening
    PhyFrame_whiteningAndBitSwap_FSK( frame->sdu.data, header->frameLength );
}

bool_t PhyFrame_analyzeFrame_FSK( phyFrame_FSK_t* frame, uint8_t recvSize, uint8_t* sduSize )
{
    *sduSize = 0;

    /* check receive size (header) */
    if( recvSize < sizeof(phyHeader_FSK_t) )
    {
        return FALSE;
    }

    /* get header */
    phyHeader_FSK_t* header = &frame->header;
    PhyFrame_bitSwap_FSK( header->byteAccess, 1 ); // LSB first

    /* calc length */
    *sduSize = PhyFrameLengthField_calcSduSize_FSK( header->frameLength );

    /* check receive size (SDU) */
    if( recvSize < *sduSize )
    {
        return FALSE;
    }

    /* check header */
    if( header->modeSwitch != PhyDataModeSwitch_DISABLE )
    {
        return FALSE;
    }

    // bit swap
    if( header->dataWhitening == PhyDataWhitening_ENABLE )
    {
        // and dewhitening
        PhyFrame_bitSwapAndWhitening_FSK( frame->sdu.data, header->frameLength ); // LSB first
    }
    else
    {
        // bit swap only
        PhyFrame_bitSwap_FSK( frame->sdu.data, header->frameLength ); // LSB first
    }

    /* get footer */
    const phyFooter_FSK_t*  footer  = PhyFrame_getFooter_FSK( frame, *sduSize );

    // calc fcs
    const uint16_t          fcs     = PhyFrame_calcFcs_FSK( frame->sdu.data, *sduSize );

    /* check footer */
    if( octet2_get(&footer->fcs) != fcs )
    {
        return FALSE;
    }

    return TRUE;
}

